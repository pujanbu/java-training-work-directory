package day4;

public class OperatorDemo2 {
	public static void main(String[] args) {
		
		//relational operator > < >= <= == !=
		int a =5;
		int b=6;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
	}

}
